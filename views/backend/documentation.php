<h1>Documentation</h1>

<p>This plugin allows you to add a news or blog section to your site.</p>

<p>It has Archive listings, an internal Tag manager, RSS producer, Categories, Permalinks and future publishing options</p>

<p>Icons in this plugin are a mixture from <a href="http://wefunction.com/2008/07/function-free-icon-set">Function</a> and <a href="http://www.tutorial9.net/resources/free-icon-pack-web-injection">Web Injection</a> icon packs.</p>

<h2>Requirements</h2>

<p>In order to run the plugin, you must have Wolf version 0.6.0 or later as it makes use of the new Dispatcher functionality in the core.</p>
<p>Requires PHP >= 5.2 as there are a couple of methods in the News Model that use the new date_parse() function</p>

